void solve()
{
    int n;
    cin >> n;
    map<int, int> m;
    int mx = -inf, a;
    for (int i = 0; i < n; i++)
    {
        cin >> a;
        m[a]++;
        mx = max(mx, m[a]);
    }
    cout << n - mx << '\n';
}
